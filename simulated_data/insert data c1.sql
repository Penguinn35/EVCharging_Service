INSERT INTO charging_point (id, status, charging_station_id)
SELECT 
    REPLACE(cs.id, 'cs-', 'cp-') || '-' || LPAD(g.n::text, 2, '0') AS id,
    (ARRAY['AVAILABLE', 'IN_USE', 'MAINTENANCE', 'OFFLINE'])[floor(random() * 4 + 1)::int] AS status,
    cs.id AS charging_station_id
FROM 
    charging_station cs,
    LATERAL generate_series(1, cs.total_points) AS g(n)
WHERE cs.id >= 'cs-vn-1001' AND cs.id <= 'cs-vn-1050';



INSERT INTO connector (id, max_power, price, type, voltage, charging_point_id)
WITH connector_mock AS (
    SELECT 
        cp.id AS point_id,
        REPLACE(cp.id, 'cp-', 'cn-') || '-' || g.n::text AS id,
        floor(random() * 5 + 1)::int AS profile_id, 
        (floor(random() * 9 + 31) * 100)::double precision AS price
    FROM 
        charging_point cp,
        LATERAL generate_series(1, floor(random() * 2 + 3)::int) AS g(n)
    -- Chỉ lấy các điểm sạc thuộc Chunk 1
    WHERE cp.charging_station_id >= 'cs-vn-1001' AND cp.charging_station_id <= 'cs-vn-1050'
)
SELECT 
    id,
    CASE 
        WHEN profile_id = 1 THEN (ARRAY[7.4, 22.0])[floor(random() * 2 + 1)::int] 
        WHEN profile_id = 2 THEN 50.0  
        WHEN profile_id = 3 THEN 150.0 
        WHEN profile_id = 4 THEN 250.0 
        ELSE (ARRAY[150.0, 250.0])[floor(random() * 2 + 1)::int] 
    END AS max_power,
    price,
    CASE 
        WHEN profile_id = 1 THEN 'Type_2'
        WHEN profile_id = 2 THEN 'CHAdeMO'
        WHEN profile_id = 3 THEN 'CCS2'
        WHEN profile_id = 4 THEN 'CCS2'
        ELSE 'Tesla'
    END AS type,
    CASE 
        WHEN profile_id = 1 THEN 220.0
        WHEN profile_id = 2 THEN 400.0
        WHEN profile_id = 3 THEN 400.0
        WHEN profile_id = 4 THEN 800.0
        ELSE 400.0
    END AS voltage,
    point_id AS charging_point_id
FROM connector_mock;