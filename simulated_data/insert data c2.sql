INSERT INTO charging_point (id, status, charging_station_id)
SELECT 
    REPLACE(cs.id, 'cs-', 'cp-') || '-' || LPAD(g.n::text, 2, '0') AS id,
    (ARRAY['AVAILABLE', 'IN_USE', 'MAINTENANCE', 'OFFLINE'])[floor(random() * 4 + 1)::int] AS status,
    cs.id AS charging_station_id
FROM 
    charging_station cs,
    LATERAL generate_series(1, cs.total_points) AS g(n)
-- Ràng buộc chỉ xử lý các trạm thuộc Chunk 2
WHERE cs.id >= 'cs-vn-1051' AND cs.id <= 'cs-vn-1100';

INSERT INTO connector (id, max_power, price, type, voltage, charging_point_id)
WITH connector_mock AS (
    SELECT 
        cp.id AS point_id,
